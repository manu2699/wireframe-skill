/**
 * Build-time generation of sketchy SVG border data-URIs.
 * Outputs app/styles/sketch-borders.css with CSS custom properties.
 * Uses rough.generator() — no DOM required.
 */

import { writeFileSync, mkdirSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import rough from "../node_modules/roughjs/bundled/rough.esm.js";

const __dir = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dir, "../app/styles/sketch-borders.css");

const ROUGHNESS = 1.5;
const STROKE_DARK = "#1a1a1a";
const STROKE_LIGHT = "#c0c0c0"; // used for light-colored elements against dark bg

function encodeSVG(svgString) {
  return (
    "url(\"data:image/svg+xml," +
    svgString
      .replace(/"/g, "'")
      .replace(/#/g, "%23")
      .replace(/\n/g, " ")
      .replace(/\s+/g, " ")
      .trim() +
    "\")"
  );
}

function makeRect(seed, stroke = STROKE_DARK) {
  const g = rough.generator();
  const d = g.rectangle(5, 5, 190, 30, {
    roughness: ROUGHNESS,
    seed,
    stroke,
    strokeWidth: 1.5,
    fill: "none",
  });
  const paths = g.toPaths(d);
  const pathEls = paths
    .map(
      (p) =>
        `<path d="${p.d}" stroke="${p.stroke || stroke}" stroke-width="${p.strokeWidth || 1.5}" fill="${p.fill || "none"}" vector-effect="non-scaling-stroke"/>`
    )
    .join("");
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 200 40" preserveAspectRatio="none">${pathEls}</svg>`;
}

function makeLineH(seed, stroke = STROKE_DARK) {
  const g = rough.generator();
  const d = g.line(2, 2, 198, 2, {
    roughness: ROUGHNESS,
    seed,
    stroke,
    strokeWidth: 1.5,
  });
  const paths = g.toPaths(d);
  const pathEls = paths
    .map(
      (p) =>
        `<path d="${p.d}" stroke="${p.stroke || stroke}" stroke-width="${p.strokeWidth || 1.5}" fill="none" vector-effect="non-scaling-stroke"/>`
    )
    .join("");
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 200 4" preserveAspectRatio="none">${pathEls}</svg>`;
}

function makeLineV(seed, stroke = STROKE_DARK) {
  const g = rough.generator();
  const d = g.line(2, 2, 2, 198, {
    roughness: ROUGHNESS,
    seed,
    stroke,
    strokeWidth: 1.5,
  });
  const paths = g.toPaths(d);
  const pathEls = paths
    .map(
      (p) =>
        `<path d="${p.d}" stroke="${p.stroke || stroke}" stroke-width="${p.strokeWidth || 1.5}" fill="none" vector-effect="non-scaling-stroke"/>`
    )
    .join("");
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 4 200" preserveAspectRatio="none">${pathEls}</svg>`;
}

const seeds = [42, 137, 256, 389];
const rects = seeds.map((s) => makeRect(s));
const rectsLight = seeds.map((s) => makeRect(s, STROKE_LIGHT));
const linesH = seeds.map((s) => makeLineH(s));
const linesHLight = seeds.map((s) => makeLineH(s, STROKE_LIGHT));
const lineV = makeLineV(42);
const lineVLight = makeLineV(42, STROKE_LIGHT);

const vars = (prefix, arr) =>
  arr.map((svg, i) => `  ${prefix}-${i + 1}: ${encodeSVG(svg)};`).join("\n");

const css = `/* AUTO-GENERATED by scripts/gen-sketch-borders.mjs — do not edit by hand */
/* Re-run: npm run gen:sketch */

:root {
${vars("  --sk-rect", rects)}
${vars("  --sk-line-h", linesH)}
  --sk-line-v-1: ${encodeSVG(lineV)};
}

.dark, [data-theme="dark"] {
${vars("  --sk-rect", rectsLight)}
${vars("  --sk-line-h", linesHLight)}
  --sk-line-v-1: ${encodeSVG(lineVLight)};
}
`;

mkdirSync(resolve(__dir, "../app/styles"), { recursive: true });
writeFileSync(OUT, css, "utf8");
console.log("gen-sketch-borders: wrote", OUT);
